proc rad_bootstrap_release_img*() =
  if not isAdmin():
    styled_echo fg_red, style_bright, &"{\"Abort\":13} :! {\"permission denied\":48}{\"1\":13}{now().format(\"hh:mm:ss tt\")}", reset_style

    rad_exit(QuitFailure)

  let img = getEnv(RAD_ENVIRONMENT_DIRECTORY_GLAUCUS) / &"{RAD_DIRECTORY_GLAUCUS}-{RAD_CERAS_S6}-{RAD_GENOME_X86_64_V3}-{now().format(\"YYYYMMdd\")}.img"

  # Create a new IMG file
  discard execCmd(&"{RAD_TOOTH_DD} bs=1M count={RAD_FILE_GLAUCUS_IMG_SIZE} if=/dev/zero of={img} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Partition the IMG file
  discard execCmd(&"{RAD_TOOTH_PARTED} {RAD_TOOTH_PARTED_FLAGS} {img} mklabel msdos {RAD_TOOTH_SHELL_REDIRECTION}")
  discard execCmd(&"{RAD_TOOTH_PARTED} {RAD_TOOTH_PARTED_FLAGS} -a none {img} mkpart primary ext4 1 {RAD_FILE_GLAUCUS_IMG_SIZE} {RAD_TOOTH_SHELL_REDIRECTION}")
  discard execCmd(&"{RAD_TOOTH_PARTED} {RAD_TOOTH_PARTED_FLAGS} -a none {img} set 1 boot on {RAD_TOOTH_SHELL_REDIRECTION}")

  # Load the `loop` module
  discard execCmd(&"{RAD_TOOTH_MODPROBE} loop {RAD_TOOTH_SHELL_REDIRECTION}")

  # Detach all used loop devices
  discard execCmd(&"{RAD_TOOTH_LOSETUP} -D {RAD_TOOTH_SHELL_REDIRECTION}")

  # Find the first unused loop device
  let
    device = execCmdEx(&"{RAD_TOOTH_LOSETUP} -f")[0].strip()
    partition = device & "p1"

  # Associate the first unused loop device with the IMG file
  discard execCmd(&"{RAD_TOOTH_LOSETUP} {device} {img} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Notify the kernel about the new partition on the IMG file
  discard execCmd(&"{RAD_TOOTH_PARTX} -a {device} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Create an `ext4` file system in the partition
  discard execCmd(&"{RAD_TOOTH_MKE2FS} {RAD_TOOTH_MKE2FS_FLAGS} ext4 {partition}")

  let mount = RAD_PATH_PKG_CONFIG_SYSROOT_DIR / RAD_PATH_MNT / RAD_DIRECTORY_GLAUCUS

  createDir(mount)

  discard execCmd(&"{RAD_TOOTH_MOUNT} {partition} {mount} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Remove `/lost+found` directory
  removeDir(mount / RAD_PATH_LOST_FOUND)

  discard rad_rsync(getEnv(RAD_ENVIRONMENT_DIRECTORY_CROSS) / RAD_PATH_PKG_CONFIG_SYSROOT_DIR, mount, RAD_TOOTH_RSYNC_RELEASE_FLAGS)

  discard rad_rsync(getEnv(RAD_ENVIRONMENT_DIRECTORY_CACHE_SOURCES) / RAD_PATH_PKG_CONFIG_SYSROOT_DIR, mount / RAD_PATH_RAD_CACHE_SOURCES, RAD_TOOTH_RSYNC_RELEASE_FLAGS)

  let path = mount / RAD_PATH_BOOT

  # Generate initramfs
  discard rad_generate_initramfs(path, true)

  # Install `grub` as the default bootloader
  createDir(path / RAD_CERAS_GRUB)

  discard rad_rsync(RAD_PATH_RAD_LIBRARY_CLUSTERS_GLAUCUS / RAD_CERAS_GRUB / RAD_FILE_GRUB_CONF_IMG, path / RAD_CERAS_GRUB / RAD_FILE_GRUB_CONF, RAD_TOOTH_RSYNC_RELEASE_FLAGS)

  discard execCmd(&"{RAD_TOOTH_GRUB_INSTALL} {RAD_TOOTH_GRUB_FLAGS} --boot-directory={mount / RAD_PATH_BOOT} --target=i386-pc {device} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Change ownerships
  discard execCmd(&"{RAD_TOOTH_CHOWN} {RAD_TOOTH_CHOWN_FLAGS} 0:0 {mount} {RAD_TOOTH_SHELL_REDIRECTION}")
  discard execCmd(&"{RAD_TOOTH_CHOWN} {RAD_TOOTH_CHOWN_FLAGS} 20:20 {mount / RAD_PATH_VAR / RAD_DIRECTORY_LOGS / RAD_PATH_WTMPD} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Clean up
  discard execCmd(&"{RAD_TOOTH_UMOUNT} {RAD_TOOTH_UMOUNT_FLAGS} {mount} {RAD_TOOTH_SHELL_REDIRECTION}")
  discard execCmd(&"{RAD_TOOTH_PARTX} -d {partition} {RAD_TOOTH_SHELL_REDIRECTION}")
  discard execCmd(&"{RAD_TOOTH_LOSETUP} -d {device} {RAD_TOOTH_SHELL_REDIRECTION}")

proc rad_bootstrap_release_iso*() =
  let
    name = &"{RAD_DIRECTORY_GLAUCUS}-{RAD_CERAS_S6}-{RAD_GENOME_X86_64_V3}-{now().format(\"YYYYMMdd\")}"
    iso = getEnv(RAD_ENVIRONMENT_DIRECTORY_GLAUCUS) / &"{name}.iso"

    path = getEnv(RAD_ENVIRONMENT_DIRECTORY_ISO) / RAD_PATH_BOOT

  # Install `grub` as the default bootloader
  removeDir(getEnv(RAD_ENVIRONMENT_DIRECTORY_ISO))
  createDir(path / RAD_CERAS_GRUB)

  discard rad_rsync(RAD_PATH_RAD_LIBRARY_CLUSTERS_GLAUCUS / RAD_CERAS_GRUB / RAD_FILE_GRUB_CONF_ISO, path / RAD_CERAS_GRUB / RAD_FILE_GRUB_CONF)

  # Generate initramfs
  discard rad_rsync(getEnv(RAD_ENVIRONMENT_DIRECTORY_GLAUCUS) / RAD_FILE_INITRAMFS_GLAUCUS, path)

  # Compress rootfs
  discard execCmd(&"{RAD_TOOTH_MKFS_EROFS} {path / RAD_FILE_ROOT_FILE_SYSTEM} {getEnv(RAD_ENVIRONMENT_DIRECTORY_CROSS)} {RAD_TOOTH_SHELL_REDIRECTION}")

  # Copy kernel
  discard rad_rsync(getEnv(RAD_ENVIRONMENT_DIRECTORY_CROSS) / RAD_PATH_BOOT / RAD_FILE_KERNEL, path)

  # Create a new ISO file
  discard execCmd(&"{RAD_TOOTH_GRUB_MKRESCUE} {RAD_TOOTH_GRUB_FLAGS} -v -o {iso} {getEnv(RAD_ENVIRONMENT_DIRECTORY_ISO)} -volid GLAUCUS {RAD_TOOTH_SHELL_REDIRECTION}")

